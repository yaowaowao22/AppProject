export function getDifficultyLabel(difficulty: Difficulty): string {
  switch (difficulty) {
    case 'easy':
      return '初級';
    case 'medium':
      return '中級';
    case 'hard':
      return '上級';
  }
}

export function getGridSizeLabel(difficulty: Difficulty): string {
  switch (difficulty) {
    case 'easy':
      return '7x7';
    case 'medium':
      return '9x9';
    case 'hard':
      return '11x11';
  }
}
