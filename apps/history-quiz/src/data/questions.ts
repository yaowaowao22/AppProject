export type QuizCount = 10 | 20 | 30;

export function getQuestionsByEra(era: Era): HistoryQuestion[] {
  if (era === '全時代') {
    return questions;
  }
  return questions.filter((q) => q.era === era);
}

function shuffleArray<T>(arr: T[]): T[] {
  const shuffled = [...arr];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

export function generateQuiz(era: Era, count: QuizCount): HistoryQuestion[] {
  const pool = getQuestionsByEra(era);
  const shuffled = shuffleArray(pool);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}
