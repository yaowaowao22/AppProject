export function getKotowazaByLevel(level: 'basic' | 'intermediate' | 'advanced' | 'ALL'): Kotowaza[] {
  if (level === 'ALL') {
    return [...kotowazaData];
  }
  return kotowazaData.filter((k) => k.level === level);
}

export function getLevelLabel(level: string): string {
  switch (level) {
    case 'basic':
      return '初級';
    case 'intermediate':
      return '中級';
    case 'advanced':
      return '上級';
    case 'ALL':
      return '全レベル';
    default:
      return level;
  }
}
