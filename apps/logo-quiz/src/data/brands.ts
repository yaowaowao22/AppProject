export const categories = [...new Set(brands.map((b) => b.category))];

export function getBrandsByCategory(category: string): BrandEntry[] {
  return brands.filter((b) => b.category === category);
}

export function getRandomBrands(count: number, category?: string): BrandEntry[] {
  const pool = category ? getBrandsByCategory(category) : brands;
  const shuffled = [...pool].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, shuffled.length));
}

export function generateChoices(
  correctBrand: BrandEntry,
  allBrands: BrandEntry[],
  choiceCount: number = 4
): { choices: string[]; correctIndex: number } {
  const wrong = allBrands
    .filter((b) => b.name !== correctBrand.name)
    .sort(() => Math.random() - 0.5)
    .slice(0, choiceCount - 1)
    .map((b) => b.name);

  const choices = [...wrong, correctBrand.name].sort(() => Math.random() - 0.5);
  const correctIndex = choices.indexOf(correctBrand.name);

  return { choices, correctIndex };
}
