export function getQuestionsByMode(mode: QuizCategory): SPIQuestion[] {
  if (mode === 'sogo') {
    return questions;
  }
  if (mode === 'gengo') {
    return questions.filter((q) => q.category === 'gengo');
  }
  return questions.filter((q) => q.category === 'higengo' || q.category === 'seikaku');
}

export function shuffleQuestions(questionList: SPIQuestion[], count: number): SPIQuestion[] {
  const shuffled = [...questionList];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled.slice(0, count);
}
