export function getPuzzlesByDifficulty(difficulty: Difficulty): Puzzle[] {
  return ALL_PUZZLES.filter((p) => p.difficulty === difficulty);
}

export function getPuzzleById(id: string): Puzzle | undefined {
  return ALL_PUZZLES.find((p) => p.id === id);
}

export function getGridSize(difficulty: Difficulty): number {
  switch (difficulty) {
    case 'easy':
      return 3;
    case 'medium':
      return 4;
    case 'hard':
      return 5;
  }
}
